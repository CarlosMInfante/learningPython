# Fundamental Data Types
# int and float
print(2+4)
print(2-4)
print(2*4)
print(2/4)
print(type(2+4))

print(type(2/4)) # 0.5 This is a float because it is not a whole number
print(type(20+1.1))

print(2 ** 3)  # This is 2 to the power of 3

print(5 % 4)  # % is the modulo operator.  This is used to get the remainder

