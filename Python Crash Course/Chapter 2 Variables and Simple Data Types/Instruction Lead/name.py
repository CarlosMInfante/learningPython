# This is assigning a string to a variable.
# The .title() is being called to capitalize the first letter of each word in the string.
name = "aba lovelace"
print(name.title())

# Calling .upper() will capitalize all the letters.
# Calling .lower() will lowercase all the letters.
name = "Ada Lovelace"
print(name.upper())
print(name.lower())
